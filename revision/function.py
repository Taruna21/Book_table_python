'''
In Python, a function is a self-contained block of code that performs a specific task and can be reused throughout your program. Functions help organize and modularize code, making it more readable, maintainable, and efficient. They also promote code reusability by allowing you to define a task once and call it multiple times from different parts of your program.

Here's how you define and use functions in Python:

python


'''

def greet(name):
    """This function greets the person passed in as a parameter."""
    print("Hello, " + name + "!")

# Call the function
greet("Alice")  # Output: Hello, Alice!

