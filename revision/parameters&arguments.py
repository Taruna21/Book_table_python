'''Parameters are defined in the function declaration
and act as placeholders for values that the function
expects to receive.Arguments are the actual values 
that are passed to a function when it is called.
Here's another example that demonstrates the 
relationship between parameters and arguments:
'''

def calculate_sum(a, b):
    return a + b

result = calculate_sum(5, 3)
print(result)  # Output: 8
