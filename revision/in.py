'''Python. Its primary purpose is to check
for membership, which means it's used to determine
whether a particular element is present within a 
collection (such as a list, tuple, string, etc.). 
The in keyword returns a Boolean
value (True or False) based on whether 
the specified element is found within the collection.'''

x =[1,2,3,54,65]

result = 3 in x
print(result)